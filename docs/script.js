// Compute level metrics
function getEcoLevel(rankPoints) {
    if (rankPoints < 100) return { title: "Eco Seedling", level: 1, max: 100, progress: rankPoints };
    if (rankPoints < 300) return { title: "Footprint Solver", level: 2, max: 300, progress: rankPoints - 100, min: 100 };
    if (rankPoints < 600) return { title: "Carbon Sentinel", level: 3, max: 600, progress: rankPoints - 300, min: 300 };
    if (rankPoints < 1000) return { title: "Renewable Champion", level: 4, max: 1000, progress: rankPoints - 600, min: 600 };
    return { title: "Earth Guardian", level: 5, max: 1500, progress: 1000, min: 1000 };
}

// Global interface updates
function recalculateDashboard() {
    const baseline = calcBaselineBreakdown(state.profile);
    ensureHistory(baseline);

    const actual = calculateActualToday(baseline, state.todayActions);

    // Compute green points based on completed challenges (100pt each), lessons read (40pt each), daily track counts (15pt each)
    let totalPoints = 50; // initial score base
    totalPoints += state.completedChallenges.length * 120;
    totalPoints += state.completedLessons.length * 40;
    totalPoints += (state.todayActions.length * 15);
    // Transport bonus if walking/biking
    if (state.profile.transportType === 'walk' || state.profile.transportType === 'bicycle') {
        totalPoints += 60;
    }
    // Shopping bonus if minimalist
    if (state.profile.shoppingFreq === 'minimalist') {
        totalPoints += 50;
    }

    const lvlData = getEcoLevel(totalPoints);

    // Dynamic Carbon Footprint savings index comparison against typical global average (16.0 kg/day)
    const savingsPercent = Math.max(0, Math.floor(((16.0 - actual.totalDaily) / 16.0) * 100));

    // Update screen components in active views
    // Today Score Circle Gauge
    const todayEmissionsText = document.getElementById('todayEmissionsVal');
    if (todayEmissionsText) todayEmissionsText.innerText = actual.totalDaily.toFixed(1);

    const percentSavingsText = document.getElementById('dashSavingsPercent');
    if (percentSavingsText) percentSavingsText.innerText = `${savingsPercent}% cleaner`;

    // Green score ring
    const carbonGoalBar = document.getElementById('carbonGoalBarFill');
    if (carbonGoalBar) {
        // Goal completion indicator based on actual vs average
        const pct = Math.min(100, Math.max(0, savingsPercent));
        carbonGoalBar.style.width = `${pct}%`;
        document.getElementById('carbonGoalText').innerText = `Green Rank Progress: ${pct}% toward True Neutral`;
    }

    // Level Header Values
    const levelTitleElem = document.getElementById('levelTitle');
    const levelNumberElem = document.getElementById('levelNumber');
    const levelBarElem = document.getElementById('levelBarFill');
    if (levelTitleElem) {
        levelTitleElem.innerText = `Lvl ${lvlData.level} - ${lvlData.title}`;
        levelNumberElem.innerText = `${totalPoints} Carbon Credits`;
        const tierMax = lvlData.max - (lvlData.min || 0);
        const barPct = Math.min(100, (lvlData.progress / tierMax) * 100);
        levelBarElem.style.width = `${barPct}%`;
    }

    // Render dashboard quick tracking items status
    const trackingBox = document.getElementById('tracklistGrid');
    if (trackingBox) {
        renderTrackingActionItems();
    }

    // Refresh active challenges view
    const challengesBox = document.getElementById('challengesContainer');
    if (challengesBox) {
        renderChallengesPage();
    }

    // Refresh Leaderboard
    const leaderboardBox = document.getElementById('leaderboardList');
    if (leaderboardBox) {
        renderLeaderboard(totalPoints);
    }

    // Refresh profile insights details
    const profileGrid = document.getElementById('profileDetailGrid');
    if (profileGrid) {
        renderProfileStats(actual);
    }

    // Refresh badges display
    renderBadges(totalPoints);

    // Update Chart visuals
    updateCharts(actual);

    // Save
    saveState();
}

