async function callLiveGeminiAPI(query) {
    const rawBaseline = calcBaselineBreakdown(state.profile);
    const systemPromptMessage = `You are the EcoTrace AI Sustainability Coach for user. User profile details:
    - Main transport: ${state.profile.transportType} commuting ${state.profile.transportDistance} km/day (Transport daily baseline: ${rawBaseline.transport.toFixed(1)} kg CO2e)
    - Electricity: ${state.profile.energyUsage} kWh monthly under ${state.profile.energyHabit} habit profile. (Energy daily baseline: ${rawBaseline.energy.toFixed(1)} kg CO2e)
    - Diet: ${state.profile.dietType} food habit (Diet daily baseline: ${rawBaseline.diet.toFixed(1)} kg CO2e)
    - Flights frequency: ${state.profile.flightFreq} flights/year.
    - Shopping: ${state.profile.shoppingFreq} style.
    - Household: ${state.profile.householdSize} member(s).
    
    Give a friendly, concise, highly targeted, actionable eco-coach recommendation based on these. Avoid verbose introductions. Use bold for highlights. Speak as a professional ecological coach. Answer the user query: "${query}"`;

    const url = `https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key=${state.geminiApiKey}`;
    
    const body = {
        contents: [{
            parts: [{ text: systemPromptMessage }]
        }]
    };

    const response = await fetch(url, {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json'
        },
        body: JSON.stringify(body)
    });

    if (!response.ok) {
        throw new Error(`Gemini API request failed: ${response.statusText}`);
    }

    const resJson = await response.json();
    return resJson.candidates[0].content.parts[0].text;
}

