// --- VISUAL FEEDBACK UTILITIES (TOAST AND CONFETTI) ---
function showToast(message, type = "info") {
    const toast = document.createElement('div');
    toast.className = `fixed bottom-24 md:bottom-8 left-1/2 -translate-x-1/2 px-5 py-3 rounded-full text-xs font-bold text-white shadow-lg transition duration-300 transform translate-y-8 opacity-0 z-50 flex items-center gap-2 ${type === "success" ? 'bg-emerald-600' : 'bg-zinc-900 dark:bg-zinc-800'}`;
    toast.innerHTML = `<span>🌱</span> <span>${message}</span>`;
    document.body.appendChild(toast);

    // Fade in
    setTimeout(() => {
        toast.classList.remove('translate-y-8', 'opacity-0');
    }, 50);

    // Fade out
    setTimeout(() => {
        toast.classList.add('translate-y-8', 'opacity-0');
        setTimeout(() => {
            document.body.removeChild(toast);
        }, 300);
    }, 2500);
}

function triggerConfetti(intensity = 0.2) {
    const colors = ['#386B1D', '#9cd67e', '#10b981', '#fbbf24', '#f59e0b'];
    const bodyHeight = document.body.scrollHeight;
    
    const count = Math.floor(60 * intensity);
    for (let i = 0; i < count; i++) {
        const c = document.createElement('div');
        c.className = 'confetti';
        c.style.backgroundColor = colors[Math.floor(Math.random() * colors.length)];
        c.style.left = `${Math.random() * 100}vw`;
        c.style.top = `${-10 - Math.random() * 20}px`;
        c.style.animationDuration = `${2 + Math.random() * 2}s`;
        c.style.animationDelay = `${Math.random() * 0.5}s`;
        document.body.appendChild(c);

        setTimeout(() => {
            document.body.removeChild(c);
        }, 4000);
    }
}

// --- THEME SWITCH ---
function toggleTheme() {
    const isDark = document.documentElement.classList.contains('dark');
    if (isDark) {
        document.documentElement.classList.remove('dark');
        localStorage.setItem('theme', 'light');
    } else {
        document.documentElement.classList.add('dark');
        localStorage.setItem('theme', 'dark');
    }
    recalculateDashboard();
}

function initTheme() {
    const saved = localStorage.getItem('theme');
    const prefersDark = window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches;
    if (saved === 'dark' || (!saved && prefersDark)) {
        document.documentElement.classList.add('dark');
    } else {
        document.documentElement.classList.remove('dark');
    }
}

// --- FLOATING COACH CHATBOT HANDLERS ---
function toggleFloatingChat(event) {
    if (event) event.stopPropagation();
    const panel = document.getElementById('floating-chat-panel');
    if (!panel) return;

    if (panel.classList.contains('hidden')) {
        panel.classList.remove('hidden');
        setTimeout(() => {
            panel.classList.remove('scale-90', 'translate-y-10', 'opacity-0');
        }, 10);
        renderFloatingChatHistory();
    } else {
        panel.classList.add('scale-90', 'translate-y-10', 'opacity-0');
        setTimeout(() => {
            panel.classList.add('hidden');
        }, 300);
    }
}


// --- INIT WORKFLOW ON LOAD ---
window.addEventListener('DOMContentLoaded', () => {
    initTheme();
    loadState();

    const onboardingSection = document.getElementById('onboardingSection');
    const mainAppSection = document.getElementById('mainAppSection');

    if (!state.onboarded) {
        // Show onboarding wizard card
        if (onboardingSection) onboardingSection.style.display = 'block';
        if (mainAppSection) mainAppSection.classList.add('hidden');
        renderOnboardingStep();
    } else {
        // Head straight to home dashboard
        if (onboardingSection) onboardingSection.style.display = 'none';
        if (mainAppSection) mainAppSection.classList.remove('hidden');
        recalculateDashboard();
        switchTab('dashboard');
    }
});
