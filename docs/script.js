// --- SCENARIO SIMULATOR MATHS & SVG DRAWING ---
function renderSimulation() {
    // Retrieve simulation slider nodes
    const sliderTransport = document.getElementById('sim_driving_slider');
    const sliderDiet = document.getElementById('sim_diet_slider');
    const sliderFlights = document.getElementById('sim_flights_slider');
    const sliderWaste = document.getElementById('sim_waste_slider');

    if (!sliderTransport) return;

    // Attach real time update handlers to sliders
    const updateSim = () => {
        const baseline = calcBaselineBreakdown(state.profile);
        const originalAnnual = baseline.totalDaily * 365.0 / 1000.0; // metric tons CO2e / year

        // Simulate modifications
        // Transportation cut: slides from 0% slider (standard) up to 100% reduction (zero car commuting)
        const transReductionRatio = parseFloat(sliderTransport.value) / 100;
        const simTransportEmissions = baseline.transport * (1 - transReductionRatio);

        // Diet modification slider index:
        // 0 -> default Mixed, 33 -> Vegan, 66 -> Vegetarian, 100 -> Meat Heavy
        const valDiet = parseInt(sliderDiet.value);
        let simDietEmissions = baseline.diet;
        if (valDiet <= 25) simDietEmissions = 1.5; // vegan diet representation
        else if (valDiet <= 60) simDietEmissions = 2.5; // vegetarian representation
        else if (valDiet <= 80) simDietEmissions = 4.5; // mixed baseline
        else simDietEmissions = 7.2; // meat heavy

        // Flights simulation
        const isFlightFree = sliderFlights.checked;
        const simFlightEmissions = isFlightFree ? 0.0 : baseline.flight;

        // Waste reduction slider
        // 0% (standard) up to 100% (composting + recycling zero landfill Scraps)
        const wasteReductionRatio = parseFloat(sliderWaste.value) / 100;
        const simWasteEmissions = baseline.waste * (1 - wasteReductionRatio);

        const simDailyTotal = simTransportEmissions + simDietEmissions + baseline.energy + simFlightEmissions + baseline.shopping + simWasteEmissions;
        const simAnnual = simDailyTotal * 365.0 / 1000.0; // metric tons CO2e / year

        const annualSaved = Math.max(0, originalAnnual - simAnnual);
        const treesEquivalent = Math.floor(annualSaved * 45.0); // 1 metric ton CO2e is offset by ~45 fully grown urban trees annually

        // Update indicators
        document.getElementById('sim_current_annual').innerText = `${originalAnnual.toFixed(2)} tons`;
        document.getElementById('sim_projected_profile').innerText = `${simAnnual.toFixed(2)} tons`;
        document.getElementById('sim_saved_indicator').innerText = `${annualSaved.toFixed(2)} Metric Tons Saved annually!`;
        document.getElementById('sim_trees_offset').innerText = `Matches the carbon absorption of roughly ${treesEquivalent} mature trees!`;

        // Update real CSS SVG Height representation Bars
        const beforeBar = document.getElementById('sim_bar_before');
        const afterBar = document.getElementById('sim_bar_after');
        if (beforeBar && afterBar) {
            const hBefore = Math.min(100, Math.max(15, (originalAnnual / 12) * 100));
            const hAfter = Math.min(100, Math.max(15, (simAnnual / 12) * 100));
            beforeBar.style.height = `${hBefore}%`;
            afterBar.style.height = `${hAfter}%`;
        }
    };

    sliderTransport.oninput = updateSim;
    sliderDiet.oninput = updateSim;
    sliderFlights.onchange = updateSim;
    sliderWaste.oninput = updateSim;

    // Run first baseline cycle render
    updateSim();
}

