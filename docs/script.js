function renderBadges(points) {
    const badgeBox = document.getElementById('badgesGrid');
    if (!badgeBox) return;

    badgeBox.innerHTML = BADGES_DEFINITIONS.map(badge => {
        const unlocked = badge.unlockReq(points, state.profile);
        return `
            <div class="p-4 rounded-3xl border flex items-center gap-3 bg-white dark:bg-zinc-900 transition-all ${unlocked ? 'border-amber-200 bg-amber-50/5/50 dark:bg-amber-950/10 shadow-sm' : 'border-gray-100 dark:border-zinc-800 opacity-40'}">
                <span class="text-3xl p-2.5 rounded-2xl bg-gray-5 text-center flex items-center justify-center">${badge.icon}</span>
                <div>
                    <h5 class="font-bold text-xs text-gray-950 dark:text-gray-100 tracking-tight leading-none">${badge.title}</h5>
                    <p class="text-[10px] text-gray-500 dark:text-zinc-400 mt-1.5 leading-tight">${badge.desc}</p>
                    <span class="text-[8px] font-bold uppercase tracking-wider block mt-1 ${unlocked ? 'text-amber-600 dark:text-amber-400' : 'text-gray-400'}">${unlocked ? 'Unlocked' : 'Locked'}</span>
                </div>
            </div>
        `;
    }).join('');
}

