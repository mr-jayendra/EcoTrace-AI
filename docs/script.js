// --- UTILITY CALCULATORS (1:1 with CarbonCalculator.kt) ---
function calcBaselineBreakdown(prof) {
    // 1. Transport
    let isNumericDist = !isNaN(parseFloat(prof.transportDistance)) && isFinite(prof.transportDistance);
    let distKm = isNumericDist ? parseFloat(prof.transportDistance) : (DEFAULT_BASINES.transportDist[prof.transportType.toLowerCase()] || 10.0);
    
    let transCoeff = 0.0;
    const typeLower = prof.transportType.toLowerCase();
    if (typeLower === 'bus') transCoeff = EMISSIONS_COEFFS.bus;
    else if (typeLower === 'train') transCoeff = EMISSIONS_COEFFS.train;
    else if (typeLower === 'motorcycle') transCoeff = EMISSIONS_COEFFS.motorcycle;
    else if (typeLower === 'car') transCoeff = EMISSIONS_COEFFS.car;
    else if (typeLower === 'walk' || typeLower === 'bicycle') transCoeff = 0.0;
    else transCoeff = EMISSIONS_COEFFS.car / 2;

    let transportEmissions = distKm * transCoeff;

    // 2. Home Energy
    let isNumericKwh = !isNaN(parseFloat(prof.energyUsage)) && isFinite(prof.energyUsage);
    let monthlyKwh = isNumericKwh ? parseFloat(prof.energyUsage) : (DEFAULT_BASINES.energyKwh[prof.energyHabit.toLowerCase()] || 360.0);
    let hhSize = parseInt(prof.householdSize) || 1;
    let energyEmissions = (monthlyKwh / 30.0) * EMISSIONS_COEFFS.electricity / Math.max(1, hhSize);

    // 3. Diet
    let dietEmissions = 4.5; // default mixed
    const dietLower = prof.dietType.toLowerCase();
    if (dietLower === 'vegan') dietEmissions = 1.5;
    else if (dietLower === 'vegetarian') dietEmissions = 2.5;
    else if (dietLower === 'mixed') dietEmissions = 4.5;
    else if (dietLower === 'meat-heavy') dietEmissions = 7.2;

    // 4. Flights (expressed as daily baseline contribution)
    let flightEmissions = 1000.0 / 365.0;
    const flightLower = prof.flightFreq.toLowerCase();
    if (flightLower === 'none') flightEmissions = 0.0;
    else if (flightLower === 'rare') flightEmissions = 500.0 / 365.0;
    else if (flightLower === 'frequent') flightEmissions = 4000.0 / 365.0;

    // 5. Shopping
    let shoppingEmissions = 4.5;
    const shopLower = prof.shoppingFreq.toLowerCase();
    if (shopLower === 'minimalist') shoppingEmissions = 1.8;
    else if (shopLower === 'average') shoppingEmissions = 4.5;
    else if (shopLower === 'shopper') shoppingEmissions = 11.0;

    // 6. Waste
    let wasteEmissions = 1.6;
    const wasteLower = prof.wasteHabit.toLowerCase();
    if (wasteLower === 'low') wasteEmissions = 0.7;
    else if (wasteLower === 'average') wasteEmissions = 1.6;
    else if (wasteLower === 'high') wasteEmissions = 3.2;

    let totalDaily = transportEmissions + energyEmissions + dietEmissions + flightEmissions + shoppingEmissions + wasteEmissions;

    return {
        totalDaily,
        transport: transportEmissions,
        energy: energyEmissions,
        diet: dietEmissions,
        flight: flightEmissions,
        shopping: shoppingEmissions,
        waste: wasteEmissions
    };
}

function calculateActualToday(baseline, actions) {
    let dailyTransport = baseline.transport;
    let dailyEnergy = baseline.energy;
    let dailyDiet = baseline.diet;
    let dailyFlight = baseline.flight;
    let dailyShopping = baseline.shopping;
    let dailyWaste = baseline.waste;

    actions.forEach(act => {
        switch (act) {
            case "Used public transport":
                dailyTransport = dailyTransport * 0.4;
                break;
            case "Walked or cycled":
                dailyTransport = 0.0;
                break;
            case "Drove a car":
                dailyTransport += 4.5;
                break;
            case "Took a flight":
                dailyFlight += 150.0;
                break;
            case "Ate more meat":
                dailyDiet = (dailyDiet * 1.3) + 1.5;
                break;
            case "Ate vegetarian meals":
                dailyDiet = dailyDiet * 0.5;
                break;
            case "Large shopping purchase":
                dailyShopping += 8.5;
                break;
            case "Higher electricity usage":
                dailyEnergy += 2.8;
                break;
            case "Other custom activity":
                dailyShopping += 1.5;
                break;
        }
    });

    return {
        totalDaily: dailyTransport + dailyEnergy + dailyDiet + dailyFlight + dailyShopping + dailyWaste,
        transport: dailyTransport,
        energy: dailyEnergy,
        diet: dailyDiet,
        flight: dailyFlight,
        shopping: dailyShopping,
        waste: dailyWaste
    };
}

// --- LOCAL STORAGE & INIT SYNCRONIZATION ---
function loadState() {
    const saved = localStorage.getItem('ecotrace_state');
    if (saved) {
        try {
            state = { ...state, ...JSON.parse(saved) };
        } catch (e) {
            console.error("Error loading state", e);
        }
    }
}

function saveState() {
    localStorage.setItem('ecotrace_state', JSON.stringify(state));
}

// Generate beautiful simulated baseline tracking history if history is empty
function ensureHistory(baseline) {
    if (!state.customLogs || state.customLogs.length === 0) {
        const dummyHistory = [];
        const days = 7;
        const now = new Date();
        for (let i = days - 1; i >= 0; i--) {
            const d = new Date(now);
            d.setDate(now.getDate() - i);
            const dateStr = d.toISOString().split('T')[0];
            // slight random variance (+- 15%)
            const variance = 0.85 + Math.random() * 0.3;
            const carbonVal = baseline.totalDaily * variance;
            dummyHistory.push({
                date: dateStr,
                footprint: parseFloat(carbonVal.toFixed(1))
            });
        }
        state.customLogs = dummyHistory;
        saveState();
    }
}

